'use client';
import { useState } from 'react';

export default function Home() {
  const [playlistUrl, setPlaylistUrl] = useState('');

  const handleGenerate = () => {
    if (!playlistUrl.includes('list=')) {
      alert("Please enter a valid YouTube Playlist URL");
      return;
    }
    // This will redirect to your course player page
    window.location.href = `/course?url=${encodeURIComponent(playlistUrl)}`;
  };

  return (
    <main className="min-h-screen bg-slate-50 flex flex-col items-center justify-center p-4">
      <div className="max-w-xl w-full bg-white shadow-2xl rounded-3xl p-8 border border-slate-100">
        <div className="flex flex-col items-center mb-8">
          <div className="bg-blue-600 p-3 rounded-2xl mb-4">
            <svg className="w-8 h-8 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M12 6.253v13m0-13C10.832 5.477 9.246 5 7.5 5S4.168 5.477 3 6.253v13C4.168 18.477 5.754 18 7.5 18s3.332.477 4.5 1.253m0-13C13.168 5.477 14.754 5 16.5 5c1.747 0 3.332.477 4.5 1.253v13C19.832 18.477 18.247 18 16.5 18c-1.746 0-3.332.477-4.5 1.253" />
            </svg>
          </div>
          <h1 className="text-3xl font-extrabold text-slate-900">Course Creator</h1>
          <p className="text-slate-500 mt-2">Convert any YouTube Playlist into a structured course</p>
        </div>

        <div className="space-y-4">
          <input 
            type="text" 
            placeholder="Paste YouTube Playlist URL here..." 
            className="w-full px-5 py-4 bg-slate-50 border border-slate-200 rounded-xl focus:ring-2 focus:ring-blue-500 outline-none transition-all text-slate-700"
            value={playlistUrl}
            onChange={(e) => setPlaylistUrl(e.target.value)}
          />
          <button 
            onClick={handleGenerate}
            className="w-full bg-blue-600 hover:bg-blue-700 text-white font-bold py-4 rounded-xl shadow-lg shadow-blue-200 transition-all active:scale-95"
          >
            Generate My Course →
          </button>
        </div>
      </div>
    </main>
  );
}
